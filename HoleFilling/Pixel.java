package HoleFilling;

public class Pixel {

	private int row;
	private int col;

	/**
	 * Describe the coordinate of the pixel in a picture
	 * @param row
	 * @param col
	 */
	public Pixel(int row, int col) {
		this.row=row;
		this.col=col;

	}

	public int getRow() {
		return row;
	}

	public int getCol() {
		return col;
	}

	

}
	
